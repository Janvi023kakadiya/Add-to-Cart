function populateProductDetails(productId) {
    const product = products[productId];
    if (!product) return;

    const fields = [
        { id: 'product-image', content: product.meta.image, isImg: true },
        { id: 'price', content: `$${product.price.toFixed(2)}` },
        { id: 'description', content: product.description },
        { id: 'category', content: product.category },
        { id: 'discount', content: `${product.discountPercentage}%` },
        { id: 'rating', content: product.rating },
        { id: 'reviews', content: product.reviews.length },
        { id: 'stock', content: product.stock },
        { id: 'brand', content: product.brand },
        { id: 'sku', content: product.sku },
        { id: 'warranty', content: product.warrantyInformation },
        { id: 'shipping', content: product.shippingInformation },
        { id: 'availability', content: product.availabilityStatus },
        { id: 'returnPolicy', content: product.returnPolicy },
        { id: 'minimumOrderQuantity', content: product.minimumOrderQuantity },
        { id: 'tags', content: product.tags.join(', ') }
    ];

    fields.forEach(({ id, content, isImg }) => {
        const element = document.getElementById(id);
        if (element) {
            if (isImg) {
                element.src = content;
            } else {
                element.textContent = content;
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const urlParams = new URLSearchParams(window.location.search);
    const productId = urlParams.get('id');
    if (productId) {
        populateProductDetails(productId);
    } else {
        console.error('Product ID is missing in the URL');
    }
});